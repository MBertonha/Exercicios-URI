#include <iostream>

using namespace std;

int main () {
    int n;
    int a,b,c,d,e,f,g,h,i,j;
    cin >> n;
    a=n*1;
    b=n*2;
    c=n*3;
    d=n*4;
    e=n*5;
    f=n*6;
    g=n*7;
    h=n*8;
    i=n*9;
    j=n*10;
    cout << "1 x "<<n<<" = "<<a<<endl;
    cout << "2 x "<<n<<" = "<<b<<endl;
    cout << "3 x "<<n<<" = "<<c<<endl;
    cout << "4 x "<<n<<" = "<<d<<endl;
    cout << "5 x "<<n<<" = "<<e<<endl;
    cout << "6 x "<<n<<" = "<<f<<endl;
    cout << "7 x "<<n<<" = "<<g<<endl;
    cout << "8 x "<<n<<" = "<<h<<endl;
    cout << "9 x "<<n<<" = "<<i<<endl;
    cout << "10 x "<<n<<" = "<<j<<endl;
    return 0;
}
